#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    pub fn test_distance() {
        let v = [
            [6.6, 6.2, 1.0],
            [9.7, 9.9, 2.0],
            [8.0, 8.3, 2.0],
            [6.3, 5.4, 1.0],
            [1.3, 2.7, 0.0],
            [2.3, 3.1, 0.0],
            [6.6, 6.0, 1.0],
            [6.5, 6.4, 1.0],
            [6.3, 5.8, 1.0],
            [9.5, 9.9, 2.0],
            [8.9, 8.9, 2.0],
            [8.7, 9.5, 2.0],
            [2.5, 3.8, 0.0],
            [2.0, 3.1, 0.0],
            [1.3, 1.3, 0.0],
        ];

        let r = distance(&v[0].to_vec(), &v[14].to_vec());
        assert_eq!(r, 0.999512076087);
    }
}
